int	ft_is_prime(int nb)
{
	int	n;

	n = 2;
	if (nb <= 1)
		return (0);
	while (n <= (nb / 2))
	{
		if (!(nb % n))
			return (0);
		else
			n += 1;
	}
	return (1);
}
