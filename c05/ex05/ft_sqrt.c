int	ft_sqrt(int nb)
{
	int	n;

	n = 0;
	if (nb < 0)
	{
		return (0);
	}
	while (n * n <= nb)
	{
		if (n * n == nb)
		{
			return (n);
		}
		else if (n >= 46340.9)
		{
			return (0);
		}
		n++;
	}
	return (0);
}
